//for循环打印乘法口诀表（正三角）
let a = 1;
let b = 1;
let s = '';
for (let i = 0; i < 9; i++) {
    for (let o = 0; o <= i; o++) {
        s += a + '*' + b + '=' + a * b + ' ';
        if (a < b) {
            a++;
        }
    }
    b++;
    a = 1;
    s += '\n';
}
console.log(s);

